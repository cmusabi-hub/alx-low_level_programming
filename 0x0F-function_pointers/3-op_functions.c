#include "3-calc.h"

/**
 * op_add - returns sum of a and b
 * @a: integer
 * @b: integer
 * Return: sum of integers
 */

int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - returns subtraction of a and b
 * @a: integer
 * @b: integer
 * Return: subtraction of integers
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication of a and b
 * @a: integer
 * @b: integer
 * Return: multiplication of integers
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division of a and b
 * @a: integer
 * @b: integer
 * Return: division of integers
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - reamainder of the divion of a by b
 * @a: integer
 * @b: integer
 * Return: modulus of integers
 */

int op_mod(int a, int b)
{
	return (a % b);
}

