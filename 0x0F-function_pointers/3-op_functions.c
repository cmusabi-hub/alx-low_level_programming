#include "3-calc.h"
/**
 * op_add - returns sum of two parameters
 * @a: integer parameter
 * @b: integer parameter
 * Return: sum of parameters
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns substraction of two parameters
 * @a: integer parameter
 * @b: integer parameter
 * Return: subtraction of parameters
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two parameters
 * @a: integer parameter
 * @b: integer parameter
 * Return: multiplication parameters
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two parameters
 * @a: integer parameter
 * @b: integer parameter
 * Return: division of parameter
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns remainder of the division of a by b
 * @a: integer parameter
 * @b: integer parameter
 * Return: remainder of the division of a by b
 */
int op_mod(int a, int b)
{
	return (a % b);
}

